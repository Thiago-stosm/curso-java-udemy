package sessao10.exercicio_de_fixacao.Entities;

public class Quarto{
    private int index;
    private String name;
    private String email;

    public Quarto(){
    }

    public int getIndex() {
        return index;
    }
    public void setIndex(int index) {
        this.index = index;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getEmail() {
        return email;
    }
    public void setEmail(String email) {
        this.email = email;
    }
}